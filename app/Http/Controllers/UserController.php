class UserController extends Controller
{
    public function register(Request $request)
    {
        $this->validator($request->all())->validate();
        $user = $this->create($request->all());
        $this->guard()->login($user);
        return response()->json([
            'user' => $user,
            'mensagem' => 'registrado com sucesso'
        ], 200);
    }
    /**
     * Get a validator for an incoming registration request.
     *
     * @param  array  $data
     * @return \Illuminate\Contracts\Validation\Validator
     */
    protected function validator(array $data)
    {
        return Validator::make($data, [
            'name' => ['required', 'string', 'max:255'],
            'email' => ['required', 'string', 'email', 'max:255', 'unique:users'],
            //'password' => ['required', 'string', 'min:4', 'confirmed'],
            // NO PASSWORD CONFIRMATION
            'password' => ['required', 'string', 'min:4'],
        ]);
    }

    /**
     * Create a new user instance after a valid registration.
     *
     * @param  array  $data
     * @return \App\User
     */
    protected function create(array $data)
    {
        return User::create([
            'name' => $data['name'],
            'email' => $data['email'],
            'password' => Hash::make($data['password']),
        ]);
    }
    protected function guard()
    {
        return Auth::guard();
    }

    public function login(Request $request)
    {
        $credentials = $request->only('email', 'password');

        if (Auth::attempt($credentials)) {
            return response()->json(['mensagem' => 'Login com sucesso', 'ok' => true]);
        } else {
            return response()->json(['mensagem' => 'E-mail ou senha inválidos', 'ok' => false]);
        }
    }

    public function logout()
    {
        Auth::logout();
        return response()->json(['mensagem' => 'Logout com sucesso'], 200);
    }

    public function forgotPassword(Request $request) {
        $request->validate(['email' => 'required|email',]);

        $status = Password::sendResetLink($request->only('email'));

        if ($status == Password::RESET_LINK_SENT) {
            return response([
                'mensagem' => 'Enviamos um e-mail para alteração de senha.',
                'ok' => true,
            ]);
        } 
        
        return [
            'mensagem' => 'Não encontramos o seu e-mail em nossa base.',
            'ok' => false
        ];
    }

    public function updatePassword(Request $request) {
        $request->validate([
            'token' => 'required',
            'email' => 'required|email',
            'password' => ['required', 'confirmed', RulesPassword::defaults()],
        ]);

        $status = Password::reset(
            $request->only('email', 'password', 'password_confirmation', 'token'),
            function ($user) use ($request) {
                $user->forceFill([
                    'password' => Hash::make($request->password),
                    'remember_token' => Str::random(60),
                ])->save();

                $user->tokens()->delete();

                event(new PasswordReset($user));
            }
        );

        if ($status == Password::PASSWORD_RESET) {
            return response([
                'mensagem'   => 'Senha alterada com sucesso',
                'ok'        => true

            ]);
        }

        return response([
            'mensagem'   => 'Não foi possível alterar a senha',
            'ok'        => false
        ]);
    }
    
    
    
    
    
